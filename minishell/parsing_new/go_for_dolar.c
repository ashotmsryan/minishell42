#include "../minishell.h"

void	go_for_dolar(int *i, int *j, char *str)
{
	if (str && str[*i] && str[*i] == 39)
	{
		(*i)++;
		(*j)++;
	}
	if (str && str[*i] && str[*i] != 39)
	{
		while (str && str[*i] && str[*i] != 39)
		{
			(*i)++;
			(*j)++;
		}
	}
	if (str && str[*i] && str[*i] == 39)
	{
		(*i)++;
		(*j)++;
	}
}
